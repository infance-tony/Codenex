// Different ways to create strings in JavaScript

// Using single quotes
let str1 = 'Hello World';

// Using double quotes
let str2 = "Hello World";

// Using template literals (backticks)
let str3 = `Hello World`;

// Using the String constructor
let str4 = new String('Hello World');

// Using String literal with escape sequences
let str5 = 'It\'s a beautiful day';

// Using template literals for multi-line strings
let str6 = `This is a
multi-line string`;

// Concatenation (though not strictly creation, but related)
let str7 = 'Hello' + ' ' + 'World';

console.log(str1, str2, str3, str4, str5, str6, str7);
